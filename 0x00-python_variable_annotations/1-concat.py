#!/usr/bin/env python3
"""Annotated function to cancatenate a two strings"""


def concat(str1: str, str2: str) -> str:
    """
    Returns result of concatenating two strings.

    Parameters:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        The concatenated string.
    """
    return str1 + str2
